package com.mycompany.spring_mvc_project_final.configuration;

import com.mycompany.spring_mvc_project_final.intercepter.GlobalIntercepter;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class InterceptorConfig implements WebMvcConfigurer {

    @Autowired
    GlobalIntercepter share;

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(share).addPathPatterns("/**");
    }
}